#!/usr/bin/env bash
# Public-facing quick verification of evidence artifacts.
# Expected output artifact: results/showcase/minimal_eval_summary.md
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

if ! command -v python >/dev/null 2>&1; then
  echo "ERROR: python not found in PATH." >&2
  exit 1
fi

OUT_DIR="${ROOT}/results/showcase"
OUT_MD="${OUT_DIR}/minimal_eval_summary.md"
mkdir -p "${OUT_DIR}"

python - <<'PY'
from __future__ import annotations

import json
from pathlib import Path

root = Path.cwd()
metrics_path = root / "results/eval/planner_base_vs_tuned/metrics.json"
baseline_path = root / "results/experiments/hybrid_replanner_eval/hybrid_calvin_debug_real_aligned_20260331T103029Z/metrics.json"
vl3b_path = root / "results/experiments/hybrid_replanner_eval/hybrid_calvin_debug_real_aligned_3b_qual_pilot_rerun/metrics.json"
vl7b_path = root / "results/experiments/hybrid_replanner_eval/hybrid_calvin_debug_real_aligned_7b_qual_pilot_rerun/metrics.json"
out_md = root / "results/showcase/minimal_eval_summary.md"

required = [metrics_path, baseline_path, vl3b_path, vl7b_path]
missing = [str(p) for p in required if not p.exists()]
if missing:
    raise SystemExit("ERROR: missing required evidence artifacts:\n- " + "\n- ".join(missing))

with metrics_path.open("r", encoding="utf-8") as f:
    m = json.load(f)
with baseline_path.open("r", encoding="utf-8") as f:
    b = json.load(f)
with vl3b_path.open("r", encoding="utf-8") as f:
    v3 = json.load(f)
with vl7b_path.open("r", encoding="utf-8") as f:
    v7 = json.load(f)

def fmt3(x: float) -> str:
    return f"{x:.3f}"

accepted_b = int(b["fallback_detail"]["revised_plan_accepted_count"])
accepted_3 = int(v3["fallback_detail"]["revised_plan_accepted_count"])
accepted_7 = int(v7["fallback_detail"]["revised_plan_accepted_count"])
n_tiny = int(b["n_episodes"])

lines = [
    "# Minimal Evaluation Summary",
    "",
    "This file is generated by `scripts/run_minimal_eval.sh` from existing repository artifacts.",
    "",
    "## Primary quantitative result (base vs tuned, n=73)",
    "",
    f"- format compliance: `{fmt3(m['format_compliance_rate_base'])} -> {fmt3(m['format_compliance_rate_tuned'])}`",
    f"- tool-use accuracy: `{fmt3(m['tool_use_accuracy_base'])} -> {fmt3(m['tool_use_accuracy_tuned'])}`",
    f"- target-match rate: `{fmt3(m['target_match_rate_base'])} -> {fmt3(m['target_match_rate_tuned'])}`",
    f"- strict task proxy: `{fmt3(m['task_completion_rate_base'])} -> {fmt3(m['task_completion_rate_tuned'])}`",
    "",
    "## Tiny controlled comparison",
    "",
    f"- baseline accepted revised plans: `{accepted_b}/{n_tiny}`",
    f"- VL-3B accepted revised plans: `{accepted_3}/{n_tiny}`",
    f"- VL-7B accepted revised plans: `{accepted_7}/{n_tiny}`",
    "",
    "## Scope note",
    "",
    "- These are repository evidence checks, not fresh benchmark reruns.",
    "- Proxy metrics are not official CALVIN environment success rates.",
]

out_md.write_text("\n".join(lines) + "\n", encoding="utf-8")
print(f"[showcase] wrote {out_md}")
PY

echo "[showcase] done. Summary written to: ${OUT_MD}"
